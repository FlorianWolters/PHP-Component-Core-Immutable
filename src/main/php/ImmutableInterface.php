namespace FlorianWolters\Component\Core;

/**
 * The interface {@see ImmutableInterface} can be implemented by a class to mark
 * its instances as **Immutable Objects**.
 *
 * The {@see ImmutableInterface} has no methods or fields and serves only to
 * identify the class with the semantics of being immutable.
 *
 * This interface is a **Marker Interface** that can be used to implement an
 * **Immutable Object**.
 *
 * @since Interface available since Release 0.1.0
 */
interface ImmutableInterface
{
}
